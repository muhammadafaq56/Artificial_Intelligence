# 1. If statement  
num = 10  
if num > 5:  
    print("The number is greater than 5")  

# 2. If-else statement  
age = 18  
if age >= 18:  
    print("You are an adult")  
else:  
    print("You are a minor")  

# 3. If-elif-else statement  
marks = 85  
if marks >= 90:  
    print("Grade: A")  
elif marks >= 80:  
    print("Grade: B")  
else:  
    print("Grade: C")  


# In Python, conditional statements control execution flow 
# based on conditions. The if statement runs a block if true,
# while if-else provides an alternative path. The if-elif-else
# structure checks multiple conditions, executing the first 
# true one. Proper indentation is essential for correct execution