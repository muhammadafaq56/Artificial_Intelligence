# 1. Printing a simple string  
print("Hello, World!")  

# 2. Printing numbers  
print(2025)  
print(3.14)  

# 3. Printing multiple values using commas  
print("Name:", "Muhammad Afaq", "Age:", 20)  

# 4. Printing using string concatenation  
print("Python " + "is " + "awesome!")  

# 5. Printing with f-strings (formatted strings)  
name = "Afaq"  
age = 20  
print(f"My name is {name} and I am {age} years old.")  

# 6. Printing a multi-line string  
print("""This is  
a multi-line  
string in Python.""")  

# 7. Printing escape sequences  
print("He said, \"Python is great!\"\nLet's learn it.")  

# 8. Printing calculations  
print("5 + 3 =", 5 + 3)  

# 9. Printing a list  
fruits = ["Apple", "Banana", "Cherry"]  
print("Fruits:", fruits)  

# 10. Printing using `sep` and `end` parameters  
print("Hello", "Python", "Learner", sep=" - ", end="!\n")  
print("Abuzar")
