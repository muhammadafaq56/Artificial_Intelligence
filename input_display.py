# 1. Input and display a string  
string = input("Enter some text: ")  
print("You entered:", string)  

# 2. Input and display an integer  
integer = int(input("Enter an integer: "))  
print("You entered the number:", integer)  

# 3. Input and display a float  
floating = float(input("Enter a decimal number: "))  
print("You entered the float:", floating)  

# WE CAN PRINT 2 STATEMENTS IN A SINGLE LINE USING SEMICOLON (;)
statement1 = "It's the first statement"
statement2 = "It's the second statement"
print(statement1); print(statement2)
